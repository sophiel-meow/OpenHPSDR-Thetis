        /// <summary>
        /// Get translation for the given text in the specified culture.
        /// Returns original text if no translation found.
        /// </summary>
        public static string GetTranslation(string text, string cultureName)
        {
            if (string.IsNullOrEmpty(text)) return text;
            if (cultureName.StartsWith("zh"))
            {
                if (_zhCN.TryGetValue(text, out string translated) && !string.IsNullOrEmpty(translated))
                    return translated;
            }
            return text;
        }

        /// <summary>
        /// Check if a translation exists for the given text.
        /// </summary>
        public static bool HasTranslation(string text, string cultureName)
        {
            if (cultureName.StartsWith("zh"))
                return _zhCN.TryGetValue(text, out string t) && !string.IsNullOrEmpty(t);
            return true;  // English is always available
        }
